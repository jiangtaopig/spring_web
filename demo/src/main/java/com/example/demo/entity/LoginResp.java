package com.example.demo.entity;

import lombok.Data;

@Data
public class LoginResp {
    private String msg;
    private int code;

    public LoginResp(String msg, int code) {
        this.msg = msg;
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }
}
